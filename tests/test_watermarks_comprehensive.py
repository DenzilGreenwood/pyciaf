class TestCanonicalJsonHashing:
    """Test canonical JSON serialization for hashing."""

    def test_canonical_json_deterministic(self):
        """Test canonical JSON produces deterministic output."""
        data1 = {"b": 2, "a": 1, "c": 3}
        data2 = {"c": 3, "a": 1, "b": 2}

        json1 = canonical_json(data1)
        json2 = canonical_json(data2)

        assert json1 == json2  # Order-independent

    def test_hash_utility_functions(self):
        """Test SHA-256 hash utility functions."""
        text = "Hello, World!"
        text_bytes = text.encode("utf-8")

        hash1 = sha256_text(text)
        hash2 = sha256_bytes(text_bytes)

        assert hash1 == hash2
        assert len(hash1) == 64  # SHA-256 hex digest


